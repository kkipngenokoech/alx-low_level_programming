#include <stdio.h>

/**
 * mul - returns the multplication of two numbers
 * @a: first paramter passed
 * @b:second parameter passed
 * Return: multiplication of the two parameters passed
 */
int mul(int a, int b)
{
	int first;
	int second;
	int result;

	first = a;
	second = b;
	result = first * second;
	return (result);
}
